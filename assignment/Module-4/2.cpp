//What is OOP? List OOP concepts 
//Object-Oriented Programming (OOP) is a programming states that models real-world 
//entities as objects. These objects have properties (attributes) and behaviors (methods). 
//OOP promotes code reusability, modularity, and maintainability.

//OOP concepts: 
//Objects: Instances of a class that represent real-world entities.
//Classes: Blueprints or templates that define the structure and behavior of objects.
//Encapsulation: Bundling data (attributes) and methods (behaviors) within a single unit (class) 
//and controlling access to them.
//Inheritance: Creating new classes (derived classes) based on existing classes (base classes), 
//inheriting their properties and methods.
//Polymorphism: The ability of objects of different classes to be treated as if they were 
//objects of a common superclass.
//Abstraction: Focusing on the essential features of an object while hiding unnecessary details.
